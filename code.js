function parseGoogleDriveFileId(link) {
  const trimmed = String(link || '').trim()
  if (!trimmed) {
    throw new Error('공개 구글 드라이브 링크를 입력해 주세요.')
  }

  if (/drive\/folders\//i.test(trimmed)) {
    throw new Error('폴더 링크는 지원되지 않습니다. 드라이브 이미지 파일 1개의 공유 링크를 넣어 주세요.')
  }

  if (/^[a-zA-Z0-9_-]{20,}$/.test(trimmed)) {
    return trimmed
  }

  const candidates = [trimmed]

  try {
    candidates.push(decodeURIComponent(trimmed))
  } catch (_error) {}

  for (const candidate of candidates) {
    try {
      const normalized = /^https?:\/\//i.test(candidate) ? candidate : 'https://' + candidate
      const parsed = new URL(normalized)
      const idFromQuery = parsed.searchParams.get('id')
      if (idFromQuery) {
        return idFromQuery
      }

      const segments = parsed.pathname.split('/').filter(Boolean)
      const fileIndex = segments.indexOf('file')
      if (fileIndex !== -1 && segments[fileIndex + 1] === 'd' && segments[fileIndex + 2]) {
        return segments[fileIndex + 2]
      }

      const dIndex = segments.indexOf('d')
      if (dIndex !== -1 && segments[dIndex + 1]) {
        return segments[dIndex + 1]
      }
    } catch (_error) {}

    const fallback = candidate.match(/[-\w]{20,}/)
    if (fallback && fallback[0]) {
      return fallback[0]
    }
  }

  throw new Error('공개 구글 드라이브 링크에서 file id를 찾지 못했습니다.')
}

async function fetchImageBytes(fileId, originalLink) {
  const candidateUrls = [
    `https://drive.usercontent.google.com/download?id=${fileId}&export=view&authuser=0&confirm=t`,
    `https://drive.usercontent.google.com/u/0/uc?id=${fileId}&export=download&confirm=t`,
    `https://drive.google.com/thumbnail?id=${fileId}&sz=w4000`,
    `https://drive.google.com/uc?export=view&id=${fileId}`,
    `https://drive.google.com/uc?export=download&id=${fileId}`,
    `https://lh3.googleusercontent.com/d/${fileId}=w4000`,
  ]

  for (const url of candidateUrls) {
    const bytes = await tryFetchImageUrl(url)
    if (bytes) {
      return bytes
    }
  }

  const extractedImageUrl = await extractDriveImageUrl(originalLink, fileId)
  if (extractedImageUrl) {
    const bytes = await tryFetchImageUrl(extractedImageUrl)
    if (bytes) {
      return bytes
    }
  }

  throw new Error('이미지 다운로드 요청에 실패했습니다.')
}

async function tryFetchImageUrl(url) {
  let response

  try {
    response = await fetch(url)
  } catch (_error) {
    return null
  }

  if (!response || !response.ok) {
    return null
  }

  const contentType =
    response.headers && typeof response.headers.get === 'function'
      ? response.headers.get('content-type') || ''
      : ''

  if (contentType && contentType.indexOf('image/') !== 0) {
    return null
  }

  const bytes = new Uint8Array(await response.arrayBuffer())
  return bytes.byteLength > 0 ? bytes : null
}

async function extractDriveImageUrl(originalLink, fileId) {
  const pageUrls = []
  if (originalLink && originalLink.trim()) {
    pageUrls.push(originalLink.trim())
  }
  pageUrls.push(`https://drive.google.com/file/d/${fileId}/view`)

  for (const pageUrl of pageUrls) {
    let response

    try {
      response = await fetch(pageUrl)
    } catch (_error) {
      continue
    }

    if (!response || !response.ok) {
      continue
    }

    const text = await response.text()
    const ogImageMatch = text.match(/property=\"og:image\" content=\"([^\"]+)\"/)
    if (ogImageMatch && ogImageMatch[1]) {
      return decodeHtmlEntities(ogImageMatch[1].replace(/\\u003d/g, '=').replace(/&amp;/g, '&'))
    }

    const downloadUrlMatch = text.match(/\"downloadUrl\":\"([^\"]+)\"/)
    if (downloadUrlMatch && downloadUrlMatch[1]) {
      return decodeHtmlEntities(downloadUrlMatch[1].replace(/\\u003d/g, '=').replace(/\\u0026/g, '&'))
    }

    const thumbnailUrlMatch = text.match(/\"(?:thumbnailUrl|urlEncodedThumbnail)\":\"([^\"]+)\"/)
    if (thumbnailUrlMatch && thumbnailUrlMatch[1]) {
      return decodeHtmlEntities(thumbnailUrlMatch[1].replace(/\\u003d/g, '=').replace(/\\u0026/g, '&'))
    }

    const directUrlMatch = text.match(/https:\/\/lh3\.googleusercontent\.com\/[^\s\"']+/)
    if (directUrlMatch && directUrlMatch[0]) {
      return decodeHtmlEntities(directUrlMatch[0].replace(/\\u003d/g, '=').replace(/&amp;/g, '&'))
    }
  }

  return null
}

function decodeHtmlEntities(value) {
  return value.replace(/&amp;/g, '&').replace(/&quot;/g, '"').replace(/&#39;/g, "'")
}

function truncateText(value) {
  const trimmed = value.trim()
  return trimmed.length >= 10 ? `${trimmed.slice(0, 9)}...` : trimmed
}

