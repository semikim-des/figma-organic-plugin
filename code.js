function postStatus(message) {
  if (figma.ui && typeof figma.ui.postMessage === 'function') {
    figma.ui.postMessage({ type: 'status', message })
  }
}

figma.ui.onmessage = async (msg) => {
  if (msg.type !== 'generate') {
    return
  }

  let currentStep = 'init'

  try {
    currentStep = 'payload'
    const { jobTitle, speakerName, dDay, imageLink, uploadedImageBytes } = msg.payload
    validateDday(dDay)

    currentStep = 'load-fonts'
    postStatus('폰트 로드 중...')
    await loadRequiredFonts()

    currentStep = 'resolve-image'
    postStatus('이미지 준비 중...')
    const userImageBytes = await resolveUserImageBytes(uploadedImageBytes, imageLink)
    const userImageHash = figma.createImage(userImageBytes).hash

    currentStep = 'resolve-origin'
    const origin = resolvePlacementOrigin()
    const createdFrames = []
    let currentX = origin.x

    currentStep = 'build-frames'
    postStatus('시안 생성 중...')
    for (const frameDef of DOCUMENT_SCHEMA.frames) {
      const frameNode = buildFrameFromDefinition(frameDef, {
        jobTitle: truncateText(jobTitle),
        speakerName: truncateText(speakerName),
        dDay: `D-${dDay}`,
        userImageHash,
      })

      frameNode.x = currentX
      frameNode.y = origin.y
      figma.currentPage.appendChild(frameNode)
      createdFrames.push(frameNode)
      currentX += frameDef.width + VARIANT_SPACING
    }

    currentStep = 'finalize'
    figma.currentPage.selection = createdFrames
    if (figma.viewport && typeof figma.viewport.scrollAndZoomIntoView === 'function') {
      figma.viewport.scrollAndZoomIntoView(createdFrames)
    }
    figma.notify('4개의 시안을 생성했습니다.')
    figma.closePlugin()
  } catch (error) {
    const baseMessage =
      error && typeof error.message === 'string' ? error.message : '알 수 없는 오류가 발생했습니다.'
    const message = '[' + currentStep + '] ' + baseMessage
    figma.ui.postMessage({ type: 'error', message })
    figma.notify(message, { error: true })
  }
}

async function loadRequiredFonts() {
  try {
    await figma.loadFontAsync(FONT_INTER_REGULAR)
    await figma.loadFontAsync(FONT_REGULAR)
    await figma.loadFontAsync(FONT_MEDIUM)
    await figma.loadFontAsync(FONT_BOLD)
  } catch (_error) {
    throw new Error('Spoqa Han Sans Neo 폰트를 불러오지 못했습니다.')
  }
}

async function resolveUserImageBytes(uploadedImageBytes, imageLink) {
  if (Array.isArray(uploadedImageBytes) && uploadedImageBytes.length > 0) {
    return new Uint8Array(uploadedImageBytes)
  }

  const driveFileId = parseGoogleDriveFileId(imageLink)
  return fetchImageBytes(driveFileId, imageLink)
}

function buildFrameFromDefinition(definition, values) {
  const frame = figma.createFrame()
  frame.name = definition.name
  frame.resize(definition.width, definition.height)
  frame.clipsContent = Boolean(definition.clipContent)
  frame.fills = parseFills(definition.fills)

  if (Array.isArray(definition.children)) {
    for (const child of definition.children) {
      const childNode = createNodeFromDefinition(child, values, false)
      if (childNode) {
        frame.appendChild(childNode)
      }
    }
  }

  applyVariantConstraints(frame, definition.id)
  return frame
}

function createNodeFromDefinition(definition, values, parentIsAutoLayout) {
  if (!definition || !definition.type) {
    return null
  }

  if (definition.name === 'LogoMark') {
    return createSvgNodeFromDefinition(definition, LOGO_WHITE_SVG, parentIsAutoLayout)
  }

  if (definition.name === 'ClockIcon') {
    return createSvgNodeFromDefinition(definition, BADGE_ICON_SVG, parentIsAutoLayout)
  }

  const type = definition.type

  if (type === 'TEXT') {
    return createTextNodeFromDefinition(definition, values, parentIsAutoLayout)
  }

  if (type === 'RECTANGLE') {
    const node = figma.createRectangle()
    applyCommonGeometry(node, definition, parentIsAutoLayout)
    node.name = resolveLayerName(definition)
    node.cornerRadius = typeof definition.cornerRadius === 'number' ? definition.cornerRadius : 0
    node.fills = parseFills(definition.fills)
    node.effects = parseEffects(definition.effects)
    return node
  }

  if (type === 'ELLIPSE') {
    if (definition.name === 'AccentCircle') {
      const container = figma.createFrame()
      applyCommonGeometry(container, definition, parentIsAutoLayout)
      container.name = resolveLayerName(definition)
      container.clipsContent = false
      container.fills = []

      const circle = figma.createEllipse()
      circle.x = 0
      circle.y = 0
      circle.resize(definition.width, definition.height)
      circle.name = 'AccentCircleFill'
      circle.fills = parseFills(definition.fills)
      circle.effects = parseEffects(definition.effects)
      container.appendChild(circle)

      const unionNode = createSvgNodeFromString(UNION_SVG)
      unionNode.name = 'Union'
      centerNodeInParent(unionNode, definition.width, definition.height)
      container.appendChild(unionNode)

      return container
    }

    const node = figma.createEllipse()
    applyCommonGeometry(node, definition, parentIsAutoLayout)
    node.name = resolveLayerName(definition)
    node.fills = parseFills(definition.fills)
    node.effects = parseEffects(definition.effects)

    return node
  }

  if (type === 'IMAGE') {
    return createImageNodeFromDefinition(definition, values, parentIsAutoLayout)
  }

  if (type === 'FRAME' || type === 'GROUP') {
    const node = figma.createFrame()
    node.name = resolveLayerName(definition)
    applyCommonGeometry(node, definition, parentIsAutoLayout)
    node.clipsContent = Boolean(definition.clipContent)
    node.cornerRadius = typeof definition.cornerRadius === 'number' ? definition.cornerRadius : 0
    node.fills = parseFills(definition.fills)
    node.effects = parseEffects(definition.effects)
    applyAutoLayout(node, definition.layout)

    if (definition.backgroundImage || definition.name === 'img') {
      node.name = 'img'
      node.fills = [imagePaint(values.userImageHash)]
    }

    if (Array.isArray(definition.children)) {
      const isAutoLayout = Boolean(definition.layout && definition.layout.mode)
      for (const child of definition.children) {
        const childNode = createNodeFromDefinition(child, values, isAutoLayout)
        if (childNode) {
          node.appendChild(childNode)
          applyChildAutoLayoutSizing(childNode, isAutoLayout)
        }
      }
    }

    return node
  }

  return null
}

function createImageNodeFromDefinition(definition, values, parentIsAutoLayout) {
  const node = figma.createRectangle()
  node.name = 'img'
  applyCommonGeometry(node, definition, parentIsAutoLayout)
  node.fills = [imagePaint(values.userImageHash)]
  return node
}

function createTextNodeFromDefinition(definition, values, parentIsAutoLayout) {
  const node = figma.createText()
  node.name = resolveLayerName(definition)

  if (!parentIsAutoLayout) {
    applyCommonGeometry(node, definition, false)
  }

  const characters = resolveTextCharacters(definition, values, node.name)
  const fontName = resolveFontName(definition.fontWeight)
  node.fontName = fontName
  node.characters = characters
  node.fontSize = typeof definition.fontSize === 'number' ? definition.fontSize : 16
  node.lineHeight = {
    unit: 'PIXELS',
    value: typeof definition.lineHeight === 'number' ? definition.lineHeight : Math.round((definition.fontSize || 16) * 1.15),
  }
  node.fills = parseFills(definition.fills)
  node.textAlignHorizontal = definition.textAlignHorizontal || 'LEFT'

  node.letterSpacing = { unit: 'PERCENT', value: -2 }

  if (definition.textTransform === 'UPPERCASE') {
    node.characters = node.characters.toUpperCase()
  }

  if (Array.isArray(definition.styledRanges) && definition.styledRanges.length > 0) {
    for (const range of definition.styledRanges) {
      if (typeof range.start === 'number' && typeof range.end === 'number' && typeof range.fontSize === 'number') {
        node.setRangeFontSize(range.start, range.end, range.fontSize)
      }
    }
  }

  node.textAutoResize = parentIsAutoLayout ? 'WIDTH_AND_HEIGHT' : 'HEIGHT'

  return node
}

function applyCommonGeometry(node, definition, parentIsAutoLayout) {
  if (!parentIsAutoLayout) {
    node.x = typeof definition.x === 'number' ? definition.x : 0
    node.y = typeof definition.y === 'number' ? definition.y : 0
  }

  if (typeof definition.width === 'number' && typeof definition.height === 'number' && 'resize' in node) {
    node.resize(definition.width, definition.height)
  }
}

function applyAutoLayout(node, layout) {
  if (!layout || !layout.mode) {
    return
  }

  node.layoutMode = layout.mode === 'VERTICAL' ? 'VERTICAL' : 'HORIZONTAL'
  node.itemSpacing = typeof layout.gap === 'number' ? layout.gap : 0

  if (layout.padding) {
    node.paddingTop = layout.padding.top || 0
    node.paddingRight = layout.padding.right || 0
    node.paddingBottom = layout.padding.bottom || 0
    node.paddingLeft = layout.padding.left || 0
  }

  node.primaryAxisAlignItems = mapPrimaryAxisAlignment(layout.justifyContent)
  node.counterAxisAlignItems = mapCounterAxisAlignment(layout.alignItems)
}

function applyChildAutoLayoutSizing(node, parentIsAutoLayout) {
  if (!parentIsAutoLayout) {
    return
  }

  const canUseHug =
    node.type === 'TEXT' ||
    (node.type === 'FRAME' && node.layoutMode && node.layoutMode !== 'NONE')

  if (!canUseHug) {
    return
  }

  if ('layoutSizingHorizontal' in node) {
    node.layoutSizingHorizontal = 'HUG'
  }

  if ('layoutSizingVertical' in node) {
    node.layoutSizingVertical = 'HUG'
  }
}

function mapPrimaryAxisAlignment(value) {
  if (value === 'center') {
    return 'CENTER'
  }
  if (value === 'flex-end') {
    return 'MAX'
  }
  if (value === 'space-between') {
    return 'SPACE_BETWEEN'
  }
  return 'MIN'
}

function mapCounterAxisAlignment(value) {
  if (value === 'center') {
    return 'CENTER'
  }
  if (value === 'flex-end') {
    return 'MAX'
  }
  return 'MIN'
}

function resolveLayerName(definition) {
  if (
    definition.name === 'img' ||
    definition.name === 'job_title' ||
    definition.name === 'speaker_name' ||
    definition.name === 'd_day'
  ) {
    return definition.name
  }

  if (definition.type === 'IMAGE' || definition.backgroundImage || definition.imageUrl) {
    return 'img'
  }

  if (definition.type === 'TEXT') {
    if (definition.name === 'DNText' || definition.name === 'd_day') {
      return 'd_day'
    }
    if (definition.characters === '직업명') {
      return 'job_title'
    }
    if (definition.characters === '연사명') {
      return 'speaker_name'
    }
  }

  return definition.name || definition.id || definition.type
}

function resolveTextCharacters(definition, values, layerName) {
  if (layerName === 'job_title') {
    return values.jobTitle
  }
  if (layerName === 'speaker_name') {
    return values.speakerName
  }
  if (layerName === 'd_day') {
    return values.dDay
  }
  return typeof definition.characters === 'string' ? definition.characters : ''
}

function resolveFontName(weight) {
  if (weight >= 700) {
    return FONT_BOLD
  }
  if (weight >= 500) {
    return FONT_MEDIUM
  }
  return FONT_REGULAR
}

function parseFills(fills) {
  if (!Array.isArray(fills) || fills.length === 0) {
    return []
  }

  const parsed = []

  for (const fill of fills) {
    if (!fill || !fill.type) {
      continue
    }

    if (fill.type === 'SOLID') {
      parsed.push({
        type: 'SOLID',
        color: parseColor(fill.color).color,
        opacity: parseColor(fill.color).opacity,
      })
      continue
    }

    if (fill.type === 'GRADIENT_LINEAR' && Array.isArray(fill.stops)) {
      parsed.push({
        type: 'GRADIENT_LINEAR',
        gradientTransform: [
          [0, 1, 0],
          [-1, 0, 1],
        ],
        gradientStops: fill.stops.map((stop) => {
          const parsedStop = parseColor(stop.color)
          return {
            position: stop.position,
            color: {
              r: parsedStop.color.r,
              g: parsedStop.color.g,
              b: parsedStop.color.b,
              a: parsedStop.opacity,
            },
          }
        }),
      })
    }
  }

  return parsed
}

function parseEffects(effects) {
  if (!Array.isArray(effects) || effects.length === 0) {
    return []
  }

  return effects
    .filter((effect) => effect && effect.type === 'DROP_SHADOW')
    .map((effect) => {
      const parsed = parseColor(effect.color)
      return {
        type: 'DROP_SHADOW',
        color: {
          r: parsed.color.r,
          g: parsed.color.g,
          b: parsed.color.b,
          a: parsed.opacity,
        },
        offset: {
          x: effect.offsetX || 0,
          y: effect.offsetY || 0,
        },
        radius: effect.blur || 0,
        visible: true,
        blendMode: 'NORMAL',
      }
    })
}

function parseColor(value) {
  if (typeof value !== 'string') {
    return { color: { r: 0, g: 0, b: 0 }, opacity: 1 }
  }

  const trimmed = value.trim()

  if (trimmed.startsWith('#')) {
    return { color: hexToRgb(trimmed), opacity: 1 }
  }

  const rgbaMatch = trimmed.match(/rgba?\(([^)]+)\)/)
  if (rgbaMatch) {
    const parts = rgbaMatch[1].split(',').map((part) => Number.parseFloat(part.trim()))
    return {
      color: {
        r: (parts[0] || 0) / 255,
        g: (parts[1] || 0) / 255,
        b: (parts[2] || 0) / 255,
      },
      opacity: parts.length > 3 ? parts[3] : 1,
    }
  }

  return { color: { r: 0, g: 0, b: 0 }, opacity: 1 }
}

function createSvgNodeFromDefinition(definition, svg, parentIsAutoLayout) {
  const node = createSvgNodeFromString(svg)
  node.name = resolveLayerName(definition)

  if (!parentIsAutoLayout) {
    node.x = typeof definition.x === 'number' ? definition.x : 0
    node.y = typeof definition.y === 'number' ? definition.y : 0
  }

  if (typeof definition.width === 'number' && typeof definition.height === 'number' && 'resize' in node) {
    node.resize(definition.width, definition.height)
  }

  return node
}

function createSvgNodeFromString(svg) {
  const node = figma.createNodeFromSvg(svg)
  node.fills = []
  return node
}

function centerNodeInParent(node, parentWidth, parentHeight) {
  const width = typeof node.width === 'number' ? node.width : 0
  const height = typeof node.height === 'number' ? node.height : 0
  node.x = (parentWidth - width) / 2
  node.y = (parentHeight - height) / 2
}

function applyVariantConstraints(frame, frameId) {
  const isCenteredVariant = frameId === 'frame-2'
  const horizontal = isCenteredVariant ? 'CENTER' : 'MIN'
  applyConstraintsRecursive(frame, horizontal)
}

function applyConstraintsRecursive(node, horizontal) {
  if ('children' in node) {
    for (const child of node.children) {
      if (child.name !== 'img' && 'constraints' in child) {
        child.constraints = {
          horizontal,
          vertical: 'MAX',
        }
      }

      applyConstraintsRecursive(child, horizontal)
    }
  }
}

function resolvePlacementOrigin() {
  if (figma.currentPage.selection.length > 0) {
    const selectedNode = figma.currentPage.selection[0]
    return { x: selectedNode.x, y: selectedNode.y }
  }

  if (figma.viewport && figma.viewport.bounds) {
    return {
      x: figma.viewport.bounds.x + 80,
      y: figma.viewport.bounds.y + 80,
    }
  }

  return {
    x: figma.viewport.center.x,
    y: figma.viewport.center.y,
  }
}

